#include	"libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int	n;

	n = 0;
	while (len > 0)
	{
		((unsigned char *)b)[n] = ((char)c);
		n++;
		len--;
	}
	return (b);
}
